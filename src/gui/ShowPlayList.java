    private void listPlaylistActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_listPlaylistActionPerformed
        Playlist selected;
        selected = user.searchPlayList(listPlaylist.getSelectedItem());
        if(library.searchSong(cancionAlbum) != null){
            selected.addSong(library.searchSong(cancionAlbum)); 
        }else if(library.searchAlbum(cancionAlbum) != null){
            selected.addAlbumPlaylistUser(library.searchAlbum(cancionAlbum));
        }        
        JOptionPane.showMessageDialog(null, "Contenido a√±adido a " + selected.getName());
        dispose();
    }//GEN-LAST:event_listPlaylistActionPerformed


    public void ShowAllListaPlaylist() {
        listPlaylist.removeAll();
        Iterator<Playlist> iteratorPlaylist = user.getPlaylist().iterator();
        while (iteratorPlaylist.hasNext()) {
            listPlaylist.add(iteratorPlaylist.next().getName());
        }
    }
    
